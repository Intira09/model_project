import json

def check_agreement(answer: str) -> str:
    found = None
    if "ไม่เห็นด้วย" in answer:
        found = "ไม่เห็นด้วย"
        score = 1
    elif "เห็นด้วย" in answer:
        found = "เห็นด้วย"
        score = 1
    else:
        score = 0
    
    result = {
        "student_answer": answer,
        "score": score,
        "found_word": found if found else "ไม่พบ"
    }
    return json.dumps(result, ensure_ascii=False, indent=2)

